        <h2>Gestion des cookies</h2>
        <hr>
        <h5>Qu’est-ce qu’un cookie ?</h5>
        <p>
            Les cookies sont des fichiers destinés à être stockés sur votre appareil (ordinateur, smartphone, tablette …), et qui seront utilisés par notre site pour interagir avec votre navigateur pour lui envoyer ou recevoir des informations. Les cookies permettent ainsi de conserver des informations tout au long de votre utilisation de notre site, ainsi que des informations relatives à vos précédentes visites.
            Seul l’émetteur d’un cookie peut recevoir et lire les informations qui sont contenues dans ce fichier.
            Il existe plusieurs types de cookies :
            Les cookies de session : ils expirent dès la fermeture du navigateur et  nous permettent de lier vos actions au cours de cette session en  particulier,
            Les cookies persistants : ils sont stockés sur votre appareil entre  les sessions de navigation et nous permettent de mémoriser vos  préférences ou actions sur plusieurs sites,
            Les cookies propriétaires : ils sont émis par le site que vous visitez,
            Les cookies tiers : ils sont émis par un site tiers, distinct du site que vous visitez
        </p>
        <h5>Quels types de cookies utilisons-nous ?</h5>
        <h5>Les cookies propriétaires</h5>
        <p>
            Dans le cadre de votre utilisation de ce site, plusieurs cookies sont  susceptibles d’être installés sur votre appareil. Ils sont utilisés afin de réaliser des statistiques de fréquentation et de navigation. Ils ne  collectent ainsi que des données agrégées de telle manière à les rendre  anonymes. Il nous est donc impossible de vous identifier, directement ou indirectement, à partir des informations collectées par ces cookies. Nous utilisons l’outil statistiques MATOMO dont la politique de confidentialité est directement consultable <a href="https://fr.matomo.org/privacy/" target="_blank">sur son site web.</a>
        </p>
        <h5>Les cookies tiers</h5>
        <p>
            Depuis janvier 2025, nous n’utilisons plus de cookies tiers, déposés par des sociétés tierces.

        <h5>Comment gérer le dépôt et la lecture de cookies sur votre appareil ?</h5>
        <p>
            Vous pouvez vous opposer à l’installation de cookies sur votre appareil en paramétrant votre navigateur ou en désactivant les cookies tiers.
        </p>
