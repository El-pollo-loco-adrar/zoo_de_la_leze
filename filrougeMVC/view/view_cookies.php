        <p>
            Pour la totalité des moyens vous permettant de vous opposer à l’installation de cookies sur vos appareils, nous vous informons que cette opposition  implique l’installation d’un cookie de refus sur votre appareil,  enregistrant votre choix. Si vous supprimez ce cookie de refus, il ne  sera plus possible pour les sites d’être avertis de votre choix.
            Les cookies installés sur votre appareil sont automatiquement supprimés 13 mois après leur installation.
